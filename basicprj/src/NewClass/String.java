package NewClass;

public class String {

	public static void main(java.lang.String[] args) {
		System.out.println("hello world");
        byte Bytevalue = 10; // integer type
        short Shortvalue = 10000;
        int intValue = 10000;
        long longValue = 1000000000l;
        
        // floating type
        float floatValue = 10.5F;
        double Doublevalue =  10.125555555555555555555555555;
        //
        char CharValue = '1';
        boolean BooleanVa1=true;// only lower case
        
        System.out.println("Byte : "+Bytevalue);
        System.out.println("Long : "+longValue);
        System.out.println("Int : "+intValue);
        System.out.println("Short : "+Shortvalue);
        System.out.println("Float : "+floatValue);
        System.out.println("Double : "+Doublevalue);
        System.out.println("Char : "+CharValue);
        System.out.println("Boolean : "+BooleanVa1);
        }

}
